#include "function_pointers.h"

/**
 * op_add - Add two numbers
 *
 * @a: First Number
 * @b: Second Number
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtract two numbers
 *
 * @a: First Number
 * @b: Second Number
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiply two numbers
 *
 * @a: First Number
 * @b: Second Number
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divide two numbers
 *
 * @a: First Number
 * @b: Second Number
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Modulus operation between two numbers
 *
 * @a: First Number
 * @b: Second Number
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
